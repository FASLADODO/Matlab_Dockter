function [Ksys] = rigidplaneframe(theta,L,E,I,A)
%%give theta in degrees
C=cosd(theta);
S=sind(theta);
EL=E/L;
F1=(12*I)/(L^2);
F2=(6*I)/L;
M11=EL*(A*C*C+F1*S*S);
M12=EL*(A-F1)*C*S;
M13=-EL*F2*S;
M14=-EL*(A*C*C+F1*S*S);
M15=-EL*(A-F1)*C*S;
M16=-EL*F2*S;
M22=EL*(A*S*S+F1*C*C);
M23=EL*F2*C;
M24=-EL*(A-F1)*C*S;
M25=-EL*(A*S*S+F1*C*C);
M26=EL*F2*C;
M33=EL*4*I;
M34=EL*F2*S;
M35=-EL*F2*C;
M36=EL*2*I;
M44=EL*(A*C*C+F1*S*S);
M45=EL*(A-F1)*C*S;
M46=EL*F2*S;
M55=EL*(A*S*S+F1*C*C);
M56=-EL*F2*C;
M66=EL*4*I;
mat=[M11,M12,M13,M14,M15,M16;M12,M22,M23,M24,M25,M26;M13,M23,M33,M34,M35,M36;M14,M24,M34,M44,M45,M46;M15,M25,M35,M45,M55,M56;M16,M26,M36,M46,M56,M66];
Ksys=mat;
